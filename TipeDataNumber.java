public class TipeDataNumber {
    public static void main(String[] args) {
        byte a = 100;
        short b = 1_000;
        int c = 10_000;
        long d = 100_000l;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
