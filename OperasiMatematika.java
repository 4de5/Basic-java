public class OperasiMatematika {
    public static void main(String[] args) {
        //operasi matematika sering digunakan pada tipe data int
        //kurang lebih sama dengan bahasa program lainnya
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
