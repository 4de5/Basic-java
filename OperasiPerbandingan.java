public class OperasiPerbandingan {
    //operasi untuk membandingkan antar data variabel
    public static void main(String[] args) {
        int a = 1;
        int b = 3;

        System.out.println(a > b); //lebih besar dari
        System.out.println(a < b); //lebih kecil dari
        System.out.println(a <= b); //lebih kecil atau sama dengan
        System.out.println(a >= b); //lebih besar atau sama dengan
        System.out.println(a != b); //tidak sama dengan
        System.out.println(a == b); //sama dengan

    }
}
