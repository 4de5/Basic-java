public class MembuatArrayDalamArray {
//  array dalam array artinya ada kamar di dalam kamar atau kotak di dalam kotak
    public static void main(String[] args) {

        String[][] anggota = {
                {"Asep", "Dwi"},
                {"Putra"},
                {"Oki", "Oke", "Oko"}
        }; //membuat array dimana sebuah anggota terdapat anggota
        System.out.println(anggota[0][0]);//Asep
        System.out.println(anggota[1][0]);//Putra
        System.out.println(anggota[2][2]);//Oko

//      gunakan KOMA untuk memisahkan antar kotak

    }
}
